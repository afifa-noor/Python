# Typecasting is the process of converting a variable from one type to another. 
# 1. int(): Converts a value to an integer
# 2. float(): Converts a value to a float
# 3. str(): Converts a value to a string
# 4. bool(): Converts a value to a boolean

#Converting a string to an integer
a="10"
b=int(a)
print(b)  
print(type(b))

#Converting a string to a float
c="3.14"
d=float(c)  
print(d)
print(type(d))

#Converting an integer to a string
e=42
f=str(e)
print(f)
print(type(f))

#converting a float to an string
g=15.99
h=(str(g))
print(h)
print(type(h))

#converting a float to integer
