#Escape sequence
#An escape sequence is a combination of characters that represents a special character
#It is used to include special characters in a string that would otherwise be difficult to include
#Common escape sequences
#\n: New line
#\t: Tab
#\\: Backslash
#Example of using escape sequences
print("Hello\nWorld")  # This will print Hello and World on separate lines
print("Hello\tWorld")  # This will print Hello and World with a tab space in between
print("This is a backslash: \\")  # This will print a single backslash
#You can also use escape sequences to include quotes in a string
print("She said, \"Hello!\"")  # This will print She said, "Hello!"
print('It\'s a nice day!')  # This will print It's a nice day!
print("This is a multi-line string using triple quotes.\nIt can span multiple lines.\nIt is useful for including special characters like new lines and tabs.\n\tThis line is indented with a tab.")     
